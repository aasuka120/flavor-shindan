function pairScore(a, b) {
  const A = TYPES[a].axes, B = TYPES[b].axes;
  let s = 0;
  if (A[0] !== B[0]) s++;
  if (A[1] === B[1]) s++;
  if (A[2] !== B[2]) s++;
  if (A[3] !== B[3]) s++;
  return s;
}

